--[[
Ctrl+↓ で下の段落に移動します

ノーマルモード、インサートモード、ターミナルモードで使用できます
空行を段落の区切りとして扱います
ターミナルモードでは下方向にスクロールします
]]

local keymap = vim.keymap.set

--[[
ノーマルモードで Ctrl+↓ を押すと下の段落に移動します

} は下方向の段落移動モーションで、次の空行まで移動します
]]
keymap("n", "<C-Down>", "}", { desc = "段落移動（下）" })

--[[
インサートモードで Ctrl+↓ を押すと下の段落に移動します

<C-o> で一時的にノーマルモードに入り、} で下の段落に移動後、自動的にインサートモードに戻ります
]]
keymap("i", "<C-Down>", "<C-o>}", { desc = "段落移動（下）" })

--[[
ターミナルモードで Ctrl+↓ を押すと下にスクロールします

<C-\><C-n> でノーマルモードに戻り <C-d> で下にスクロールします
スクロール後はノーマルモードのままになります
]]
keymap("t", "<C-Down>", "<C-\\><C-n><C-d>", { noremap = true, desc = "下にスクロール" })
