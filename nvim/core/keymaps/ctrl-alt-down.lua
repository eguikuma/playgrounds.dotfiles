--[[
Ctrl+Alt+↓ で下の段落に移動します

Ctrl+↓ と同じ動作です
ノーマルモード、インサートモードで使用できます
空行を段落の区切りとして扱います
]]

local keymap = vim.keymap.set

--[[
ノーマルモードで Ctrl+Alt+↓ を押すと下の段落に移動します

} は下方向の段落移動モーションで、次の空行まで移動します
]]
keymap("n", "<C-M-Down>", "}", { desc = "段落移動（下）" })

--[[
インサートモードで Ctrl+Alt+↓ を押すと下の段落に移動します

<C-o> で一時的にノーマルモードに入り、} で下の段落に移動後、自動的にインサートモードに戻ります
]]
keymap("i", "<C-M-Down>", "<C-o>}", { desc = "段落移動（下）" })
