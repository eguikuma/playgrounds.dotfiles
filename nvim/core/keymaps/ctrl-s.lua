--[[
Ctrl+S でファイルを保存します

ノーマルモード、ビジュアルモード、インサートモードで使用できます
]]

local keymap = vim.keymap.set

--[[
ノーマルモードで Ctrl+S を押すとファイルを保存します

<cmd>w<cr> でコマンドモードの :w（write/保存）を実行します
]]
keymap("n", "<C-s>", "<cmd>w<cr>", { desc = "保存" })

--[[
ビジュアルモードで Ctrl+S を押すとファイルを保存します

<Esc> でノーマルモードに戻り、<cmd>w<cr> で保存を実行します
]]
keymap("v", "<C-s>", "<Esc><cmd>w<cr>", { desc = "保存" })

--[[
インサートモードで Ctrl+S を押すとファイルを保存します

<C-o> で一時的にノーマルモードに入り、:w<CR> で保存を実行後、自動的にインサートモードに戻ります
カーソル位置も完全に保持されます
]]
keymap("i", "<C-s>", "<C-o>:w<CR>", { desc = "保存" })
