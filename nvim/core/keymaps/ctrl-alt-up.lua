--[[
Ctrl+Alt+↑ で上の段落に移動します

Ctrl+↑ と同じ動作です
ノーマルモード、インサートモードで使用できます
]]

local keymap = vim.keymap.set

--[[
ノーマルモードで Ctrl+Alt+↑ を押すと上の段落に移動します

{ は上方向の段落移動モーションで、直前の空行まで移動します
]]
keymap("n", "<C-M-Up>", "{", { desc = "段落移動（上）" })

--[[
インサートモードで Ctrl+Alt+↑ を押すと上の段落に移動します

<C-o> で一時的にノーマルモードに入り、{ で上の段落に移動後、自動的にインサートモードに戻ります
]]
keymap("i", "<C-M-Up>", "<C-o>{", { desc = "段落移動（上）" })
