--[[
Ctrl+Z で直前の操作を元に戻します

ノーマルモード、ビジュアルモード、インサートモードで使用できます
]]

local keymap = vim.keymap.set

--[[
ノーマルモードで Ctrl+Z を押すと直前の操作を元に戻します

u（undo）で直前の変更を取り消します
]]
keymap("n", "<C-z>", "u", { desc = "元に戻す" })

--[[
ビジュアルモードで Ctrl+Z を押すと直前の操作を元に戻します

<Esc> でノーマルモードに戻り、u（undo）で直前の変更を取り消します
]]
keymap("v", "<C-z>", "<Esc>u", { desc = "元に戻す" })

--[[
インサートモードで Ctrl+Z を押すと直前の操作を元に戻します

<C-o> で一時的にノーマルモードに入り、u で元に戻す操作を実行後、自動的にインサートモードに戻ります
カーソル位置も完全に保持されます
]]
keymap("i", "<C-z>", "<C-o>u", { desc = "元に戻す" })
