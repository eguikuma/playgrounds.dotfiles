--[[
Ctrl+↑ で上の段落に移動します

ノーマルモード、インサートモード、ターミナルモードで使用できます
ターミナルモードでは上方向にスクロールします
]]

local keymap = vim.keymap.set

--[[
ノーマルモードで Ctrl+↑ を押すと上の段落に移動します

{ は上方向の段落移動モーションで、直前の空行まで移動します
]]
keymap("n", "<C-Up>", "{", { desc = "段落移動（上）" })

--[[
インサートモードで Ctrl+↑ を押すと上の段落に移動します

<C-o> で一時的にノーマルモードに入り、{ で上の段落に移動後、自動的にインサートモードに戻ります
]]
keymap("i", "<C-Up>", "<C-o>{", { desc = "段落移動（上）" })

--[[
ターミナルモードで Ctrl+↑ を押すと上にスクロールします

<C-\><C-n> でノーマルモードに戻り <C-u> で上にスクロールします
スクロール後はノーマルモードのままになります
]]
keymap("t", "<C-Up>", "<C-\\><C-n><C-u>", { noremap = true, desc = "上にスクロール" })
