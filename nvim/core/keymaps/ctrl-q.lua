--[[
Ctrl+Q で矩形選択モードに入ります

ノーマルモード、ビジュアルモード、インサートモードで使用できます
Ctrl+V がペーストに使用されているため、矩形選択の代替キーとして設定します
]]

local keymap = vim.keymap.set

--[[
ノーマルモードで Ctrl+Q を押すと矩形選択モード（Visual Block Mode）に入ります

<C-v> は本来の矩形選択キーですが、Windows 環境では Ctrl+V がペーストに使用されるため、Ctrl+Q を代替として使用します
]]
keymap("n", "<C-q>", "<C-v>", { noremap = true, desc = "矩形選択" })

--[[
ビジュアルモードで Ctrl+Q を押すと矩形選択モードに切り替わります

通常のビジュアルモード（v）や行選択モード（V）から矩形選択に切り替えられます
]]
keymap("v", "<C-q>", "<C-v>", { noremap = true, desc = "矩形選択に切り替え" })

--[[
インサートモードで Ctrl+Q を押すとノーマルモードに戻って矩形選択モードに入ります

<Esc> でインサートモードを抜けて、<C-v> で矩形選択モードに入ります
]]
keymap("i", "<C-q>", "<Esc><C-v>", { noremap = true, desc = "矩形選択" })
