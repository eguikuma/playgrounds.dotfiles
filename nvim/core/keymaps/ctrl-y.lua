--[[
Ctrl+Y で元に戻した操作をやり直します

ノーマルモード、ビジュアルモード、インサートモードで使用できます
]]

local keymap = vim.keymap.set

--[[
ノーマルモードで Ctrl+Y を押すと元に戻した操作をやり直します

<C-r>（Ctrl+R、redo）で取り消した変更を再適用します
]]
keymap("n", "<C-y>", "<C-r>", { desc = "やり直す" })

--[[
ビジュアルモードで Ctrl+Y を押すとやり直します

<Esc> でノーマルモードに戻り、<C-r>（redo）で取り消した変更を再適用します
]]
keymap("v", "<C-y>", "<Esc><C-r>", { desc = "やり直す" })

--[[
インサートモードで Ctrl+Y を押すとやり直します

<C-o> で一時的にノーマルモードに入り、<C-r> でやり直し操作を実行後、自動的にインサートモードに戻ります
カーソル位置も完全に保持されます
]]
keymap("i", "<C-y>", "<C-o><C-r>", { desc = "やり直す" })
