--[[
Ctrl+V でシステムクリップボードからペーストします

ノーマルモード、ビジュアルモード、インサートモードで使用できます
]]

local keymap = vim.keymap.set

--[[
ノーマルモードで Ctrl+V を押すとクリップボードからペーストします

"+ でシステムクリップボードレジスタを指定し、gP でカーソル位置の前に貼り付けます
gP は貼り付け後にカーソルを貼り付けたテキストの末尾に移動させます
]]
keymap("n", "<C-v>", '"+gP', { desc = "ペースト" })

--[[
ビジュアルモードで Ctrl+V を押すと選択範囲をクリップボードの内容で置換します

"_c でブラックホールレジスタに削除してインサートモードに入り、<C-r>+ でクリップボードの内容を挿入し、<Esc> でノーマルモードに戻ります
<Right> は <Esc> 後にカーソルが1文字左に移動するのを補正するために必要です

ブラックホールレジスタ（"_）を使うことで、削除したテキストがレジスタを上書きするのを防ぎます
]]
keymap("v", "<C-v>", '"_c<C-r>+<Esc><Right>', { desc = "ペースト" })

--[[
インサートモードで Ctrl+V を押すとクリップボードからペーストします

<C-r>+ でインサートモード中にシステムクリップボードの内容をカーソル位置に挿入します
]]
keymap("i", "<C-v>", "<C-r>+", { desc = "ペースト" })
