--[[
Esc でノーマルモードに戻ります

ノーマルモード、ビジュアルモード、インサートモードで使用できます
]]

local keymap = vim.keymap.set

--[[
ノーマルモードで Esc を押すと検索ハイライトを消します

nohlsearch コマンドで検索結果のハイライト表示を一時的に無効にします
次に検索を行うと再びハイライトが表示されます
]]
keymap("n", "<Esc>", "<cmd>nohlsearch<cr>", { desc = "検索ハイライトを消す" })

--[[
ビジュアルモードで Esc を押すとノーマルモードに戻ります

選択を解除してノーマルモードに戻ります
]]
keymap("v", "<Esc>", "<Esc>", { desc = "ノーマルモードに戻る" })

--[[
インサートモードで Esc を押すとノーマルモードに戻ります

入力を確定してノーマルモードに戻ります
]]
keymap("i", "<Esc>", "<Esc>", { desc = "ノーマルモードに戻る" })

--[[
ターミナルモードで Esc を押すとノーマルモードに戻ります

<C-\><C-n> はターミナルモードを終了する標準のキーシーケンスです
]]
keymap("t", "<Esc>", "<C-\\><C-n>", { desc = "ターミナルモードを終了" })
